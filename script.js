/* DOUBLE TAP FIREWORK */

let lastTap = 0;

document.addEventListener(
"touchend",
function(event){

const currentTime =
new Date().getTime();

const tapLength =
currentTime - lastTap;

if(tapLength < 300 && tapLength > 0){

createFirework(

event.changedTouches[0].clientX,

event.changedTouches[0].clientY
);

}

lastTap = currentTime;

}
);



/* DESKTOP CLICK */

document.addEventListener(
"dblclick",
function(event){

createFirework(
event.clientX,
event.clientY
);

}
);



